package tddClass.kata;

public class Arithmetic {
    public int arithmeticSquare(int firstNumber) {
        int square = firstNumber * firstNumber;
        return square;
    }

    public int arithmeticSquareNumber(int secondNumber) {
        int square = secondNumber * secondNumber;
        return  square;
    }

    public int sumOfSquare(int firstNumber, int secondNumber) {
        int sum = firstNumber * firstNumber + secondNumber * secondNumber;
        return sum;
    }

    public int differenceOfSum(int firstNumber, int secondNumber) {
        int difference = firstNumber * firstNumber - secondNumber * secondNumber;
        return difference;

    }
}
